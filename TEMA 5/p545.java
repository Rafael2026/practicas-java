// Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás
// utilizando un bucle while.
public class p545 {
    public static void main(String[] args)
    {
        int cont = 320;
        while (cont =160){
            System.out.println(cont);
            cont = cont-20;
        }
    }
}